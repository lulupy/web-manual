var isTrue = function(value, callback) {
  if (value === true) {
    callback(null, "Value was true.");
  }
  else {
    callback(new Error("Value is not true!"));
  }
}

var callback = function (error, value) {
  if (error) {
    return console.log(error);
  }
  console.log(value);
}

isTrue(true, callback);
isTrue(false, callback);